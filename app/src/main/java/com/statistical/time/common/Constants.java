    public  static  String  getThemeByPosition(int  position,int style){
        if (style==1){
            if (position<=starRes.length){
                return "明星";
            }else if (position<=starRes.length+catongRes.length){
                return "卡通";
            }else {
                return  "风景";
            }
        }else{
            return "背景";
        }


    }
    public  static  int   getRealPosition(int  position,int style){
        if (style==1){
            if (position<=starRes.length){
                return position;
            }else if (position<=starRes.length+catongRes.length){
                return position-starRes.length;
            }else {
                return position-starRes.length-catongRes.length;
            }
        }else if (style==2){
            if (position>type2Res.length){
                return 1;
            }else{
                return position;
            }

        }else if (style==3){
            if (position>type3Res.length){
                return 1;
            }else{
                return position;
            }

        }else {
            if (position>type4Res.length){
                return 1;
            }else{
                return position;
            }

        }


    }
    public  static int[] getRes(int  position,int  style){
        if (style==1){
            if (position<=starRes.length){
                return starRes[position-1];
            }else if (position<=starRes.length+catongRes.length){
                return catongRes[position-starRes.length-1];
            }else {
                return scapeRes[position-starRes.length-catongRes.length-1];
            }

        }else if (style==2){
            if (position>type2Res.length){
                return type2Res[0];
            }else{
                return type2Res[position-1];
            }

        }else if (style==3){
            if (position>type3Res.length){
                return type3Res[0];
            }else{
                return type3Res[position-1];
            }

        }else {
            if (position>type4Res.length){
                return type4Res[0];
            }else{
                return type4Res[position-1];
            }

        }


    }





